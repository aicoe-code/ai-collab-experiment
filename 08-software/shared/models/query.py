"""Query entity model — canonical representation of a data clarification request.

Implements: FR-022 (Query Management)
Aligns with: query.json schema, 05-data-models/canonical/
"""

from __future__ import annotations

from datetime import datetime
from enum import Enum
from uuid import UUID

from pydantic import BaseModel, Field


class QueryStatus(str, Enum):
    """Lifecycle states for a data query."""

    OPEN = "open"
    ANSWERED = "answered"
    CLOSED = "closed"
    CANCELLED = "cancelled"
    REOPENED = "reopened"


class QueryPriority(str, Enum):
    """Query priority levels."""

    LOW = "low"
    MEDIUM = "medium"
    HIGH = "high"
    CRITICAL = "critical"


class Query(BaseModel):
    """Canonical Query model.

    Represents a data clarification request (DCR) raised
    during clinical data review.
    """

    query_id: UUID = Field(..., description="Unique query identifier")
    study_id: UUID = Field(..., description="Reference to parent Study")
    subject_id: UUID = Field(..., description="Reference to Subject")
    site_id: UUID = Field(..., description="Site associated with the query")
    crf_page: str = Field(
        ..., max_length=100, description="CRF page or form identifier"
    )
    field_name: str = Field(
        ..., max_length=200, description="Data field triggering the query"
    )
    query_text: str = Field(
        ..., min_length=1, max_length=2000, description="Query description"
    )
    status: QueryStatus = Field(
        default=QueryStatus.OPEN, description="Current query status"
    )
    priority: QueryPriority = Field(
        default=QueryPriority.MEDIUM, description="Query priority"
    )
    raised_by: str = Field(
        ..., max_length=200, description="Person or system that raised the query"
    )
    assigned_to: str | None = Field(
        default=None, max_length=200, description="Person assigned to resolve"
    )
    response: str | None = Field(
        default=None, max_length=2000, description="Response to the query"
    )
    responded_by: str | None = Field(
        default=None, max_length=200, description="Person who responded"
    )
    responded_at: datetime | None = Field(
        default=None, description="Timestamp of response"
    )
    auto_generated: bool = Field(
        default=False, description="Whether query was auto-generated by system"
    )
    created_at: datetime = Field(
        default_factory=datetime.utcnow, description="Record creation timestamp"
    )
    updated_at: datetime = Field(
        default_factory=datetime.utcnow, description="Last update timestamp"
    )

    model_config = {"from_attributes": True}
